import { NextResponse } from "next/server";
import {
  runPipeline,
  loadPipelineStatus,
  savePipelineStatus,
  parseOrdersFromText,
  ArticleOrder,
} from "@/lib/ai-pipeline";

export async function GET() {
  const orders = loadPipelineStatus();
  return NextResponse.json({ orders });
}

export async function POST(request: Request) {
  const body = await request.json();
  const { orders: rawOrders, apiKey, autoPublish = true, ordersText } = body as {
    orders?: { topic: string; notes?: string }[];
    ordersText?: string;
    apiKey: string;
    autoPublish?: boolean;
  };

  if (!apiKey) {
    return NextResponse.json({ error: "Missing Grok API key" }, { status: 400 });
  }

  let newOrders: { topic: string; notes?: string }[] = [];
  if (ordersText) {
    newOrders = parseOrdersFromText(ordersText);
  } else if (rawOrders) {
    newOrders = rawOrders;
  }

  if (!newOrders.length) {
    return NextResponse.json({ error: "No orders provided" }, { status: 400 });
  }

  const existing = loadPipelineStatus();
  const articleOrders: ArticleOrder[] = newOrders.map((o) => ({
    id: `ord_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`,
    topic: o.topic,
    notes: o.notes,
    status: "queued" as const,
    createdAt: new Date().toISOString(),
  }));

  savePipelineStatus([...existing, ...articleOrders]);

  const config = { autoPublish, grokApiKey: apiKey };

  // Fire and forget Ã¢â‚¬â€ runs sequentially in background
  (async () => {
    for (const order of articleOrders) {
      await runPipeline(order, config);
    }
  })();

  return NextResponse.json({
    message: `${articleOrders.length} order(s) submitted`,
    ids: articleOrders.map((o) => o.id),
  });
}

export async function DELETE() {
  const orders = loadPipelineStatus();
  const active = orders.filter((o) => !["done", "error"].includes(o.status));
  savePipelineStatus(active);
  return NextResponse.json({ cleared: orders.length - active.length, remaining: active.length });
}
